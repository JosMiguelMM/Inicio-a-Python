#Operaciones matematicas

#EL DOBLE SIGNO DE MULTIPLICACION QUIERE DECIR POTENCIA
print(2 ** 3)
print(2 ** 3.)
print(2. ** 3)
print(2. ** 3.)
print()

#MULTIPLICACION
print(2 * 3)
print(2 * 3.)
print(2. * 3)
print(2. * 3. ,"\n")

#DIVISION
print(6 / 3)
print(6 / 3.)
print(6. / 3)
print(6. / 3.,"\n")

#DIVISION CON VALORES EXACTOS
print(6 // 5)
print(6 // 3.)
print(6. // 4)
print(6. // 7.,"\n")


#MODULO
print(14 % 4,"\n")

#SUMA Y RESTA
print(-4 + 4)
print(-4. + 8)


